#Arithmetic Operators-Simple calculations

num1 = 45
num2 = 20
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) #Modulus-Returns remainder

#Comparison operators
x = 78
y = 60
print(x< y)
print(x> y)
print(x<= y)
print(x>= y)
print(x== y)
print(x !=y)

#Assignment operators-Used to Assign values to  a variables
number = 40
print(number)

a = 35
